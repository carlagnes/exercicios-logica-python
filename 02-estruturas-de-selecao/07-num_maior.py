# Escreva um algoritmo que receba dois valores inteiros distintos e indique qual deles é o maior.

print("\n===== VERIFICA MAIOR NÚMERO =====")
num1 = int(input("\nDigite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

if num1>num2:
  print(f"\nO número {num1} é maior que {num2}.")
else:
  print(f"\nO número {num2} é maior que {num1}.")

